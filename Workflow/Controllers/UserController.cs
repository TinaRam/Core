        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("UserId,Username,Password,FirstName,LastName,Email,PhoneNumber,Role,About")] User user)
        {
            if (ModelState.IsValid)
            {
                _context.Add(user);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        // GET: User/Edit/5
        public void Edit(int UserId, string Username, String Password, string FirstName, string LastName, string Email, string PhoneNumber, int Role, string About)
        {
            User user = _context.User.Find(UserId);
            user.Username = Username;
            user.Password = Password;
            user.FirstName = FirstName;
            user.LastName = LastName;
            user.Email = Email;
            user.PhoneNumber = PhoneNumber;
            user.Role = Role;
            user.About = About;

            _context.SaveChanges();

            Response.Redirect("/User/Details/" + UserId);
        }

